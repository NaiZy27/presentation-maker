def build_prompt(topic: str, language: str, requirements: str) -> str:
    requirements_block = (
        f"\nДОПОЛНИТЕЛЬНЫЕ ТРЕБОВАНИЯ: {requirements}"
        if requirements and requirements.lower() != "без особых требований"
        else ""
    )

    return f"""Ты - профессиональный помощник по созданию академических презентаций для РЭУ им. Г.В. Плеханова.

ЗАДАЧА: Сгенерировать контент для 10 слайдов презентации на тему: «{topic}».

ЯЗЫК КОНТЕНТА: {language}. Весь текст (header, text) пиши строго на этом языке.

ПРАВИЛА:
1. Ровно 10 слайдов - не меньше, не больше.
2. header - заголовок слайда, не длиннее 80 символов.
3. text - готовый академический текст слайда, не длиннее 1200 символов. Пиши сам текст, а не описание того, что должно быть написано. Не используй фразы вроде "рассматривается", "описывается", "анализируется" — пиши конкретные факты, определения, данные напрямую.
4. image_query - поисковый запрос для Unsplash. ВСЕГДА на английском языке, независимо от языка контента. Конкретный, описательный, 3–6 слов.
5. Академический стиль: факты, термины, логическая структура от общего к частному.
6. НЕ начинай строки с дефисов, тире или маркеров списка (-, –, •) — шаблон уже содержит маркеры.
6. Слайды должны образовывать связный нарратив: введение → основная часть → заключение.{requirements_block}

ВАЖНО: image_query - ТОЛЬКО английский язык. Это обязательное требование."""
